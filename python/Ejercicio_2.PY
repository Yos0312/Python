# EJERCICIO 2 ÁREA Y PERÍMETRO DE UN CÍRCULO 

#ENTRADA DE DATOS
PI = 3.1416
radio = float(input("Radio del círculo: "))



#PROCESOS 
área = PI * (radio ** 2)
perímetro = 2 * radio * PI




#SALIDA DE DATOS 
print("El área es ",round(área,2))
print("El perímetro es ",round(perímetro,2))
